SELECT 
--  D.CUIIO,
  D.RIND,
  SUM(D.COL8)  AS COL8,
  SUM(D.COL9)  AS COL9,
  SUM(D.COL10) AS COL10,
  SUM(D.COL11) AS COL11
FROM CIS2.VW_DATA_ALL D
WHERE
  D.PERIOADA IN (:pPERIOADA) AND 
  D.FORM_VERS = :pFORM_VERS AND    
  (:pID_MDTABLE = :pID_MDTABLE) AND
  D.CUATM_FULL LIKE '%' || :pCOD_CUATM || ';%' AND
  D.FORM IN (50) AND 
  D.CAPITOL IN (1058) AND
  D.RIND IN (
    '10112.1.1','10112.1.2','10113.1.1','10114.1.2',
    '10912.1.2','10912.1.3','10913.2.1','10914.1.4'
  )
GROUP BY 
--  D.CUIIO,
  D.RIND
ORDER BY 
 -- D.CUIIO,
  CASE D.RIND
    WHEN '10112.1.1' THEN 1
    WHEN '10112.1.2' THEN 2
    WHEN '10113.1.1' THEN 3
    WHEN '10114.1.2' THEN 4
    WHEN '10912.1.2' THEN 5
    WHEN '10912.1.3' THEN 6
    WHEN '10913.2.1' THEN 7
    WHEN '10914.1.4' THEN 8
    ELSE 999
  END;
