SELECT 
  D.CUIIO,
  D.RIND,
  SUM(D.COL4)  AS COL4,
  SUM(D.COL5)  AS COL5,
  SUM(D.COL6) AS COL6,
  SUM(D.COL7) AS COL7,
  SUM(D.COL8) AS COL8,
  SUM(D.COL9) AS COL9
FROM CIS2.VW_DATA_ALL D
WHERE
  D.PERIOADA IN (:pPERIOADA) AND 
  D.FORM_VERS = :pFORM_VERS AND    
  (:pID_MDTABLE = :pID_MDTABLE) AND
  D.CUATM_FULL LIKE '%' || :pCOD_CUATM || ';%' AND
  D.FORM IN (50) AND 
  D.CAPITOL IN (1058) AND
  D.RIND IN (
    '20112.1.1','20112.1.2','20113.1.1','20114.1.2',
    '20912.1.2','20912.1.3','20913.2.1','20914.1.4'
  )
GROUP BY 
  D.CUIIO,
  D.RIND
ORDER BY 
  D.CUIIO,
  CASE D.RIND
    WHEN '20112.1.1' THEN 1
    WHEN '20112.1.2' THEN 2
    WHEN '20113.1.1' THEN 3
    WHEN '20114.1.2' THEN 4
    WHEN '20912.1.2' THEN 5
    WHEN '20912.1.3' THEN 6
    WHEN '20913.2.1' THEN 7
    WHEN '20914.1.4' THEN 8
    ELSE 999
  END;
