SELECT 
    DISTINCT 
    CUIIO,
    FORM,
    CAPITOL_DEN,
    RIND,
    MAX(USER_NAME) USER_NAME,
    MAX(DATA_REG)  DATA_REG,
    SUM(COL1) AS COL1,
    SUM(COL2) AS COL2,
    SUM(COL3) AS COL3,
    SUM(COL4) AS COL4,
    SUM(COL5) AS COL5
    
 
FROM CIS2.VW_DATA_ALL
 
WHERE 

FORM  IN (33)
 
AND PERIOADA = 1066 AND 

CUIIO LIKE 40851742||'%'
 




GROUP BY 
CUIIO,
RIND,
    FORM,
    CAPITOL_DEN