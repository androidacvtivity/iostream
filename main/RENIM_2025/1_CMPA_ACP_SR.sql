
SELECT 
D.UNIT_CODE,
D.RIND,
SUM(D.COL1) AS COL1

FROM
  VW_DATA_ALL_GC  D
  
                
WHERE
  (D.PERIOADA IN (:pPERIOADA))  AND   
  (D.FORM =:pFORM) AND
  (D.FORM_VERS =:pFORM_VERS) AND 
  (:pID_MDTABLE =:pID_MDTABLE) AND
  D.FORM IN (61)  AND
  D.CAPITOL IN (1114)
  
  GROUP BY
  D.UNIT_CODE,
  D.RIND
  
  
  HAVING 
  D.RIND IN ('1')
  ORDER BY
  D.UNIT_CODE,
  D.RIND