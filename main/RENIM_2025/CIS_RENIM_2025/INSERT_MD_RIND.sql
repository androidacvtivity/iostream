SELECT 
  L.ID_MD,
  L.FORM,
  L.FORM_VERS,
  L.CAPITOL,   
  L.CAPITOL_VERS,
  L.RIND,
  492 RIND_VERS,
  L.DENUMIRE,
  L.DECIMAL_POS,
  L.COL_ACTIV,
  L.ORDINE,
  L.DATA_REG,
  L.STATUT,
  L.DINAMIC
FROM CIS.MD_RIND L INNER JOIN (
  SELECT

  RIND,
  MAX(RIND_VERS) RIND_VERS
FROM CIS.MD_RIND

WHERE capitol = 94
  AND capitol_vers = 384
GROUP BY 
RIND

) R  ON R.RIND = L.RIND AND R.RIND_VERS = L.RIND_VERS
WHERE capitol = 94
  AND capitol_vers = 384
  AND (
       L.RIND LIKE '101313000%' OR
       L.RIND LIKE '101315950%' OR
       L.RIND LIKE '101315951%' OR
