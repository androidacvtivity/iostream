       L.RIND LIKE '201463600%' OR
       L.RIND LIKE '201463800%' OR
       L.RIND LIKE '202015900%' OR
       L.RIND LIKE '265181000%' OR
       L.RIND LIKE '282513330%' OR
       L.RIND LIKE '282513350%' OR
       L.RIND LIKE '282513600%' OR
       L.RIND LIKE '289939300%'
  )
  
   
 -- AND L.STATUT <> '3'


ORDER BY 
 L.RIND,
  L.RIND_VERS
  
  

