
SELECT 
L.CUIIO
FROM VW_EDU_1_2025 L LEFT JOIN (

SELECT  
DISTINCT D.CUIIO,
SUM(D.COL14) AS COL14
  
FROM 
  CIS2.VW_DATA_ALL D
WHERE 
  (D.PERIOADA =:pPERIOADA) AND
  (D.FORM =:pFORM) AND
  D.FORM = 40   AND 
  D.CAPITOL IN 1022 AND 
  D.RIND IN ('19') 

  
GROUP BY
D.CUIIO

ORDER BY
SUM(D.COL14) ) R ON R.CUIIO = L.CUIIO 


WHERE
R.CUIIO IS NULL 