SELECT 
  NULL ID_MD,
  10 FORM,
  1999 FORM_VERS,
  93 CAPITOL,   
  2000 CAPITOL_VERS,
  L.RIND,
  2012 AS RIND_VERS,
  L.DENUMIRE,
  '1111111111' DECIMAL_POS,
  '11R1111111' COL_ACTIV,
  26||ROWNUM  ORDINE,
  SYSDATE DATA_REG,
  L.STATUT,
  L.DINAMIC
FROM CIS.MD_RIND L
INNER JOIN (
  SELECT RIND, MAX(RIND_VERS) AS RIND_VERS
  FROM CIS.MD_RIND
  WHERE CAPITOL = 94
    AND CAPITOL_VERS = 384
  GROUP BY RIND
) R
  ON R.RIND = L.RIND
 AND R.RIND_VERS = L.RIND_VERS
WHERE L.CAPITOL = 94
  AND L.CAPITOL_VERS = 384
  AND (
       L.RIND LIKE '101313100%' OR
       L.RIND LIKE '101315960%' OR
       L.RIND LIKE '101315961%' OR
       L.RIND LIKE '101315962%' OR
       L.RIND LIKE '101315963%' OR
       L.RIND LIKE '101315964%' OR
       L.RIND LIKE '101315969%' OR
       L.RIND LIKE '104153310%' OR
       L.RIND LIKE '162116110%' OR
       L.RIND LIKE '201463610%' OR
       L.RIND LIKE '201463810%' OR
       L.RIND LIKE '202015910%' OR
       L.RIND LIKE '265181100%' OR
       L.RIND LIKE '282513340%' OR
       L.RIND LIKE '282513360%' OR
       L.RIND LIKE '282513610%' OR
       L.RIND LIKE '289939310%'
  )
  -- AND L.STATUT <> '3'
ORDER BY 
  L.RIND,
  L.RIND_VERS;
