SELECT
   'Unul din Rind. (711,7111,7112,7113,7114,7115,7116,712,7121,7122,7123)  COL 1 - trebuie > 0 '
  

    
   AS REZULTAT
  
FROM
 CIS2.VW_DATA_ALL D

WHERE
  (D.PERIOADA IN(:PERIOADA)      ) AND
  (D.CUIIO=:CUIIO                ) AND
  (D.CUIIO_VERS=:CUIIO_VERS      OR :CUIIO_VERS = -1) AND
  (D.FORM_VERS=:FORM_VERS ) AND
  (:CAPITOL_VERS=:CAPITOL_VERS   OR :CAPITOL_VERS <>  :CAPITOL_VERS) AND
  (D.ID_MD=:ID_MD                OR :ID_MD = -1) AND
  (:CAPITOL=:CAPITOL             OR :CAPITOL <>  :CAPITOL) AND
  (:FORM = :FORM) AND  
  D.FORM    IN(5) AND D.CAPITOL IN (309,1221) 
  
  
HAVING
 NVAL(SUM(CASE WHEN D.CAPITOL IN (309) AND D.RIND IN ('890') THEN D.COL1 ELSE 0 END)) > 0
 
 AND 
 
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('711') THEN D.COL1 ELSE 0 END))  + 
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7111') THEN D.COL1 ELSE 0 END)) + 
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7112') THEN D.COL1 ELSE 0 END)) + 
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7113') THEN D.COL1 ELSE 0 END)) +
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7114') THEN D.COL1 ELSE 0 END)) +
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7115') THEN D.COL1 ELSE 0 END)) +
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7116') THEN D.COL1 ELSE 0 END)) +
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('712') THEN D.COL1 ELSE 0 END))  +
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7121') THEN D.COL1 ELSE 0 END)) +
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7122') THEN D.COL1 ELSE 0 END)) +
 NVAL(SUM(CASE WHEN D.CAPITOL IN (1221) AND D.RIND IN ('7123') THEN D.COL1 ELSE 0 END))  =  0