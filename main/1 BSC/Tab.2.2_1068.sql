WHERE
  C.PRGS IN (2) AND
  C.CODUL NOT IN ('9800000')
ORDER BY
  T.ORDINE,
  B.RIND,
  A.RIND
) A
LEFT JOIN
(
SELECT
  D.RIND_S,
  D.RIND_D,
  D.ID_MD_S,
  D.ID_MD_D,
  CC.CODUL AS CUATM,
  ROUND(SUM(CASE WHEN D.CAPITOL  IN (1010) THEN D.COL1_S  ELSE NULL END),1) AS COL1,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R000_1  + DD.R000_3 + DD.R000_5)   ELSE NULL END),1) AS COL2,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R999_1  + DD.R999_3 + DD.R999_5)   ELSE NULL END),1) AS COL3,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R000_2  + DD.R000_4 + DD.R000_6)   ELSE NULL END),1) AS COL4,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R999_2  + DD.R999_4 + DD.R999_6)   ELSE NULL END),1) AS COL5,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R000_2 +  DD.R000_4  + DD.R000_6) /  (CIS2.NOZERO(DD.R000_1  + DD.R000_3 + DD.R000_5)) ELSE NULL END),1) AS COL6,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R999_2  + DD.R999_4 + DD.R999_6) /  (CIS2.NOZERO(DD.R999_1  + DD.R999_3  + DD.R999_5))  ELSE NULL END),1) AS COL7,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN ((DD.R000_1 + DD.R000_3+DD.R000_5) + (DD.R000_2 + DD.R000_4 + DD.R000_6))   /   (CIS2.NOZERO(DD.R000_1 + DD.R000_3+DD.R000_5)) ELSE NULL END),1) AS COL8,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN ((DD.R999_1 + DD.R999_3+DD.R999_5) + (DD.R999_2 + DD.R999_4 + DD.R999_6))  /  (CIS2.NOZERO(DD.R999_1 + DD.R999_3+DD.R999_5))  ELSE NULL END),1) AS COL9

FROM
(
SELECT
  A.RIND AS RIND_S,
  B.RIND AS RIND_D,
  A.CUIIO,
  C.CODUL AS CUATM,
  A.CAPITOL,
  A.ID_MD AS ID_MD_S,
  B.ID_MD AS ID_MD_D,
  SUM(A.COL1) AS COL1_S,
  SUM(B.COL1) AS COL1_D
FROM
(
SELECT
      D.CUIIO,
      D.CUATM,
      D.RIND,
      D.ID_MD,
      D.CAPITOL,
      CIS2.NVAL(D.COL1) AS COL1
    FROM
       CIS2.VW_DATA_ALL D
      
         INNER JOIN
         (
           SELECT P.PERIOADA FROM CIS2.VW_MD_PERIOADA P 
           WHERE P.PERIOADA = :pPERIOADA 
    
           UNION   
   
           SELECT P.PERIOADA -1 FROM CIS2.VW_MD_PERIOADA P INNER JOIN CIS2.VW_MD_PERIOADA T ON(P.ANUL=T.ANUL AND P.TIP_PERIOADA=7) 
           WHERE T.PERIOADA = :pPERIOADA
   
        ) P ON(D.PERIOADA=P.PERIOADA)
    WHERE
      D.FORM IN (20) AND
      D.CAPITOL IN (1010) AND
      D.RIND IN ('1','2','3','4','5','6')
       AND (D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%') 
       
) A
INNER JOIN
(      
 SELECT
      D.CUIIO,
      D.CUATM,
      D.RIND,
      D.ID_MD,
      D.CAPITOL,
      CIS2.NVAL(D.COL1) AS COL1
    FROM
       CIS2.VW_DATA_ALL D
      
         INNER JOIN
         (
           SELECT P.PERIOADA FROM CIS2.VW_MD_PERIOADA P 
           WHERE P.PERIOADA = :pPERIOADA 
    
           UNION   
   
           SELECT P.PERIOADA -1 FROM CIS2.VW_MD_PERIOADA P INNER JOIN CIS2.VW_MD_PERIOADA T ON(P.ANUL=T.ANUL AND P.TIP_PERIOADA=7) 
           WHERE T.PERIOADA = :pPERIOADA
   
        ) P ON(D.PERIOADA=P.PERIOADA)
    WHERE
      D.FORM IN (20) AND
      D.CAPITOL IN (1010) AND
      D.RIND NOT IN ('00','--','1','2','3','4','5','6','113')
            
