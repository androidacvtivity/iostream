GROUP BY
  T.CUATM,
  T.FULL_CODE,
  T.CUATM_DENUMIRE,
  T.DENUMIRE,
  T.ORDINE
  --D.RIND_S
--  D.RIND_D
--  D.ID_MD_S,
--  D.ID_MD_D
ORDER BY
  T.FULL_CODE,
  T.ORDINE
  ) 
  VB 
   
  
  UNION 
  
  
  SELECT
  A.CUATM                         AS  CUATM,
  A.FULL_CODE                     AS  FULL_CODE,
  A.CUATM_DENUMIRE                AS  CUATM_DENUMIRE,
  A.DENUMIRE                      AS  DENUMIRE,
  TO_NUMBER(A.ORDINE||'.'||A.RIND_S)         AS  ORDINE,
  B.COL1                          AS  COL1,
  B.COL2                          AS  COL2,
  B.COL3                          AS  COL3,
  B.COL4                          AS  COL4,
  B.COL5                          AS  COL5,
  ROUND(B.COL4/CIS2.NOZERO(B.COL2),1)               AS  COL6,
  ROUND(B.COL5/CIS2.NOZERO(B.COL3),1)               AS  COL7,
  ROUND((B.COL2+B.COL4)/CIS2.NOZERO(B.COL2),1)      AS  COL8,
  ROUND((B.COL3+B.COL5)/CIS2.NOZERO(B.COL3),1)      AS  COL9  
FROM
(
SELECT
         C.CODUL AS CUATM,
         C.FULL_CODE,
        C.DENUMIRE AS CUATM_DENUMIRE,    
        B.RIND AS RIND_D,
  A.RIND AS RIND_S,
  B.ID_MD AS ID_MD_D,
  A.ID_MD AS ID_MD_S,
  B.CAPITOL,
  A.DENUMIRE,
  T.ORDINE
FROM
(
SELECT
  R.RIND,
  R.DENUMIRE,
  R.ID_MD,
  R.CAPITOL,
