SELECT
  :pPERIOADA AS PERIOADA,                                                    
  :pFORM AS FORM,                                                            
  :pFORM_VERS AS FORM_VERS,                                                  
  :pID_MDTABLE AS ID_MDTABLE,                                                
  :pCOD_CUATM AS COD_CUATM,   
  
  FF.FULL_CODE AS NR_SECTIE,                                                         
  FF.CUATM_DENUMIRE AS NUME_SECTIE, 
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' AS NR_SECTIE2,
  '0' AS NUME_SECTIE2,
   --FF.COL2||'~'||FF.ORDINE AS NR_ROW,
    CASE WHEN INSTR(TO_CHAR(FF.COL2)||'~'||ROWNUM,'.',1) = 1 THEN 0||FF.COL2||'~'||ROWNUM ELSE FF.COL2||'~'||ROWNUM END AS NR_ROW,
   FF.ORDINE AS ORDINE,
  '0001111' AS DECIMAL_POS,
   FF.DENUMIRE AS NUME_ROW,
   FF.COL3  AS COL1,
   FF.COL4  AS COL2,
   FF.COL5  AS COL3,
   FF.COL6  AS COL4,
   FF.COL7  AS COL5,
   FF.COL8  AS COL6,
   FF.COL9  AS COL7
FROM
(
SELECT 
  VB.CUATM            AS CUATM,
  VB.FULL_CODE        AS FULL_CODE,
  VB.CUATM_DENUMIRE   AS CUATM_DENUMIRE,
  VB.DENUMIRE         AS DENUMIRE,
  VB.ORDINE           AS ORDINE, 
  VB.COL1             AS  COL1,    
  VB.COL2             AS  COL2,
  VB.COL3             AS  COL3,
  VB.COL4             AS  COL4,
  VB.COL5             AS  COL5,
  ROUND(VB.COL4    /  CIS2.NOZERO(VB.COL2),1)               AS  COL6,
  ROUND(VB.COL5    /  CIS2.NOZERO(VB.COL3),1)               AS  COL7,
  ROUND((VB.COL2   +  VB.COL4) / CIS2.NOZERO(VB.COL2),1)     AS  COL8,
  ROUND((VB.COL3   +  VB.COL5) / CIS2.NOZERO(VB.COL3),1)     AS  COL9  
FROM (
SELECT
  T.CUATM             AS CUATM,
  T.FULL_CODE         AS FULL_CODE,
  T.CUATM_DENUMIRE    AS CUATM_DENUMIRE,
  T.DENUMIRE          AS DENUMIRE,
  T.ORDINE            AS ORDINE, 
  ROUND(SUM(CASE WHEN D.CAPITOL  IN (1010) THEN D.COL1_S  ELSE NULL END),1) AS COL1,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R000_1  + DD.R000_3 + DD.R000_5) ELSE NULL END),1) AS COL2,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R999_1  + DD.R999_3 + DD.R999_5) ELSE NULL END),1) AS COL3,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R000_2  + DD.R000_4 + DD.R000_6) ELSE NULL END),1) AS COL4,
  ROUND(SUM(CASE WHEN DD.CAPITOL IN (1017) THEN (DD.R999_2  + DD.R999_4 + DD.R999_6) ELSE NULL END),1) AS COL5
