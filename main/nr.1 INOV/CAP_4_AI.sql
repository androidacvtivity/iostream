FROM CIS2.VW_DATA_ALL D
INNER JOIN CIS2.RENIM R 
    ON R.CUIIO = D.CUIIO 
   AND R.CUIIO_VERS = D.CUIIO_VERS

LEFT JOIN BS_AGG BS 
    ON BS.CUIIO = D.CUIIO

LEFT JOIN BSS_AGG BSS 
    ON BSS.CUIIO = D.CUIIO

WHERE D.PERIOADA = :pPERIOADA
  AND D.FORM     = :pFORM
  AND EXISTS (
        SELECT 1
        FROM CIS2.VW_CL_CUATM C
        WHERE C.CODUL = D.CUATM
  )

GROUP BY 
    D.CUIIO,
    R.IDNO,
    R.CUATM,
    R.CAEM2,
    R.DENUMIRE,
    BS.PERS_CURENT_2024,
    BS.CA_2024,
    BSS.PERS_CURENT_2024,
    BSS.CA_2024

ORDER BY 
    R.CUATM;