WITH BS_AGG AS (
    SELECT
        CUIIO,
        MAX(PERS_CURENT_2024) AS PERS_CURENT_2024,
        MAX(CA_2024)          AS CA_2024
    FROM USER_BANCU.INOV_1_24
    GROUP BY CUIIO
),
BSS_AGG AS (
    SELECT
        CUIIO,
        MAX(PERS_CURENT_2024) AS PERS_CURENT_2024,
        MAX(CA_2024)          AS CA_2024
    FROM USER_BANCU.INOV_1_24_2
    GROUP BY CUIIO
)
