    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.4'  THEN NVL(D.COL3, 0) ELSE 0 END) AS R_3_1_4_C3, 
 
    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.5'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_3_1_5_C1,
    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.5'  THEN NVL(D.COL2, 0) ELSE 0 END) AS R_3_1_5_C2,
    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.5'  THEN NVL(D.COL3, 0) ELSE 0 END) AS R_3_1_5_C3,
    
    
    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.6'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_3_1_6_C1,
    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.6'  THEN NVL(D.COL2, 0) ELSE 0 END) AS R_3_1_6_C2,
    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.6'  THEN NVL(D.COL3, 0) ELSE 0 END) AS R_3_1_6_C3,
    
    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.7'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_3_1_7_C1,
