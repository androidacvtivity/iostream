    D.CUIIO,
    R.IDNO,
    R.CUATM,
    R.CAEM2,
    R.DENUMIRE,

    BS.PERS_CURENT_2024,
    BS.CA_2024,

    BSS.PERS_CURENT_2024 AS PERS,
    BSS.CA_2024          AS CA,

    MAX(
        CASE 
            WHEN D.CAPITOL = 100 
             AND D.RIND NOT IN ('ER', '-') 
            THEN D.RIND  
        END
    ) AS CAP_SR,

    SUM(CASE WHEN D.CAPITOL = 1042 AND D.RIND = '3.1.1'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_3_1_1_C1,
