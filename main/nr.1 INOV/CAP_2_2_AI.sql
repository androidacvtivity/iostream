WITH BS_AGG AS (
    SELECT
        CUIIO,
        MAX(PERS_CURENT_2024) AS PERS_CURENT_2024,
        MAX(CA_2024)          AS CA_2024
    FROM USER_BANCU.INOV_1_24
    GROUP BY CUIIO
),
BSS_AGG AS (
    SELECT
        CUIIO,
        MAX(PERS_CURENT_2024) AS PERS_CURENT_2024,
        MAX(CA_2024)          AS CA_2024
    FROM USER_BANCU.INOV_1_24_2
    GROUP BY CUIIO
)
SELECT
    D.CUIIO,
    R.IDNO,
    R.CUATM,
    R.CAEM2,
    R.DENUMIRE,

    BS.PERS_CURENT_2024,
    BS.CA_2024,

    BSS.PERS_CURENT_2024 AS PERS,
    BSS.CA_2024          AS CA,

    MAX(
        CASE 
            WHEN D.CAPITOL = 100 
             AND D.RIND NOT IN ('ER', '-') 
            THEN D.RIND  
        END
    ) AS CAP_SR,

    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.1'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_2_2_1_C1,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.1'  THEN NVL(D.COL2, 0) ELSE 0 END) AS R_2_2_1_C2,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.1'  THEN NVL(D.COL3, 0) ELSE 0 END) AS R_2_2_1_C3,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.1'  THEN NVL(D.COL4, 0) ELSE 0 END) AS R_2_2_1_C4,
    
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.2'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_2_2_2_C1,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.2'  THEN NVL(D.COL2, 0) ELSE 0 END) AS R_2_2_2_C2,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.2'  THEN NVL(D.COL3, 0) ELSE 0 END) AS R_2_2_2_C3,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.2'  THEN NVL(D.COL4, 0) ELSE 0 END) AS R_2_2_2_C4,
    
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.3'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_2_2_3_C1,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.3'  THEN NVL(D.COL2, 0) ELSE 0 END) AS R_2_2_3_C2,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.3'  THEN NVL(D.COL3, 0) ELSE 0 END) AS R_2_2_3_C3,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.3'  THEN NVL(D.COL4, 0) ELSE 0 END) AS R_2_2_3_C4,
    
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.4'  THEN NVL(D.COL1, 0) ELSE 0 END) AS R_2_2_4_C1,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.4'  THEN NVL(D.COL2, 0) ELSE 0 END) AS R_2_2_4_C2,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.4'  THEN NVL(D.COL3, 0) ELSE 0 END) AS R_2_2_4_C3,
    SUM(CASE WHEN D.CAPITOL = 1048 AND D.RIND = '2.2.4'  THEN NVL(D.COL4, 0) ELSE 0 END) AS R_2_2_4_C4
    
    
    
    

FROM CIS2.VW_DATA_ALL D
INNER JOIN CIS2.RENIM R 
    ON R.CUIIO = D.CUIIO 
   AND R.CUIIO_VERS = D.CUIIO_VERS

LEFT JOIN BS_AGG BS 
    ON BS.CUIIO = D.CUIIO

LEFT JOIN BSS_AGG BSS 
    ON BSS.CUIIO = D.CUIIO

WHERE D.PERIOADA = :pPERIOADA
  AND D.FORM     = :pFORM
  AND EXISTS (
        SELECT 1
        FROM CIS2.VW_CL_CUATM C
        WHERE C.CODUL = D.CUATM
  )

GROUP BY 
    D.CUIIO,
    R.IDNO,
    R.CUATM,
    R.CAEM2,
    R.DENUMIRE,
    BS.PERS_CURENT_2024,
    BS.CA_2024,
    BSS.PERS_CURENT_2024,
    BSS.CA_2024

ORDER BY 
    R.CUATM;