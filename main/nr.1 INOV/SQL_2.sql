
SELECT 
    CUIIO,
    RIND,
    SUM(COL1) AS COL1


FROM USER_EREPORTING.VW_DATA_ALL_PRIMIT 

WHERE


PERIOADA = 2013 AND 

FORM IN (48) 
  
  
  --AND CUIIO IN (38735017)
  
  AND CAPITOL = 1042
  
  GROUP BY 
    CUIIO,
    RIND
    
    HAVING 
    
    SUM(COL1) > 1
    
    
    ORDER BY 
    
      CUIIO,
      RIND