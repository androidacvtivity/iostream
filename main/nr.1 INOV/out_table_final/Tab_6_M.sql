--INSERT INTO CIS2.TABLE_OUT 
--(
--  PERIOADA,
--  FORM,
--  FORM_VERS,
--  ID_MDTABLE,
--  COD_CUATM,
--  NR_SECTIE,
--  NUME_SECTIE,
--  NR_SECTIE1,
--  NUME_SECTIE1,
--  NR_SECTIE2,
--  NUME_SECTIE2,
--  NR_ROW,
--  ORDINE,
--  DECIMAL_POS,
--  NUME_ROW,  
--  COL1, COL2, COL3, COL4, COL5, COL6, COL7
--)

SELECT 
  :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,    
  NR_TABLE AS NR_SECTIE,
  TABLE_DENUMIRE AS NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' AS NR_SECTIE2,
  '0' AS NUME_SECTIE2, 
  COL1||'~'||ROWNUM  NR_ROW,
  ROWNUM AS ORDINE,
  '00000000' AS DECIMAL_POS, 
  RIND_DENUMIRE AS NUME_ROW,

  /* mapping final nemodificat; îl facem sa produca cerin?a prin setarea nivelului interior:
     final COL1 = prev.COL2 (TOTAL), final COL2..COL5 = prev.COL3..COL6 (1.6.1..1.6.4), final COL6..COL7 = 0 */
  COL2 AS COL1, 
  COL3 AS COL2,
  COL4 AS COL3,
  COL5 AS COL4,
  COL6 AS COL5, 
  COL7 AS COL6,
  COL8 AS COL7

FROM
(
  SELECT
    A.NR_TABLE,
    A.TABLE_DENUMIRE,
    A.RIND_DENUMIRE,
    A.ORDINE,
    A.RIND,
    ROUND(A.COL1,0) AS COL1,
    ROUND(A.COL2,0) AS COL2,
    ROUND(A.COL3,0) AS COL3,
    ROUND(A.COL4,0) AS COL4,
    ROUND(A.COL5,0) AS COL5,
    ROUND(A.COL6,0) AS COL6,
    ROUND(A.COL7,0) AS COL7,
    ROUND(A.COL8,0) AS COL8
  FROM
  (
    SELECT 
      NR_TABLE,
      TABLE_DENUMIRE,
      RIND_DENUMIRE,
      ORDINE,
      RIND,
      COL1, COL2, COL3, COL4, COL5, COL6, COL7, COL8
    FROM
    (
      SELECT
        A.NR_TABLE,
        A.TABLE_DENUMIRE,
        A.RIND_DENUMIRE,
        A.ORDINE,
        A.RIND,

        /* Las structura identica dar schimb semnifica?ia coloanelor pentru a respecta cerin?a:
           - A.COL2 = TOTAL (suma 1.6.1..1.6.4)
           - A.COL3..A.COL6 = 1.6.1..1.6.4
           - A.COL7, A.COL8 = 0 (filler)
           Strat de "rounding" pastreaza forma existenta: 
             prev.COL2 = A.COL1, prev.COL3 = A.COL2, ... prev.COL8 = A.COL7.
           De aceea definim mai jos A.COL1..A.COL7 corespunzator. */

        /* prev.COL1 (nefolosit în mapping-ul final) – îl pastram ca total clasic daca vrei; nu afecteaza ie?irea finala */
        ROUND( 
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.1') AND NVAL(A1.COL1)=1 THEN A1.CUIIO END) +
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.2') AND NVAL(A1.COL1)=1 THEN A1.CUIIO END) +
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.3') AND NVAL(A1.COL1)=1 THEN A1.CUIIO END) +
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.4') AND NVAL(A1.COL1)=1 THEN A1.CUIIO END)
        ,0) AS COL1,

        /* prev.COL2 (=> final COL1) = TOTAL (1.6.1..1.6.4) */
        (
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.1') AND NVAL(A1.COL1)=1
                 AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
                 AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
               THEN A1.CUIIO END) +
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.2') AND NVAL(A1.COL1)=1
                 AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
                 AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
               THEN A1.CUIIO END) +
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.3') AND NVAL(A1.COL1)=1
                 AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
                 AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
               THEN A1.CUIIO END) +
          COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.4') AND NVAL(A1.COL1)=1
                 AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
                 AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
               THEN A1.CUIIO END)
        ) AS COL2,

        /* prev.COL3..prev.COL6 (=> final COL2..COL5) = 1.6.1..1.6.4 */
        COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.1') AND NVAL(A1.COL1)=1
               AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
               AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
             THEN A1.CUIIO END) AS COL3,

        COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.2') AND NVAL(A1.COL1)=1
               AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
               AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
             THEN A1.CUIIO END) AS COL4,

        COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.3') AND NVAL(A1.COL1)=1
               AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
               AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
             THEN A1.CUIIO END) AS COL5,

        COUNT(DISTINCT CASE WHEN A1.RIND IN ('1.6.4') AND NVAL(A1.COL1)=1
               AND ((ROUND(BS.PERS_IT) >= A.PERS_INIT AND ROUND(BS.PERS_IT) <= A.PERS_FINAL) OR ROUND(BS.PERS_IT) IS NULL)
               AND (A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,1,1)||'%' OR A.RIND_C LIKE '%'||SUBSTR(A1.CAEM2,2,2)||'%')
             THEN A1.CUIIO END) AS COL6,

        /* prev.COL7..prev.COL8 (=> final COL6..COL7) = 0 */
        0 AS COL7,
        0 AS COL8

      FROM
      (
        /* sursa A1: exact ca la tine, doar rîndurile schimbate la 1.6.x */
        SELECT
          D.ANUL,
          D.CUIIO,
          D.CUATM,
          D.CAEM2,
          D.CAPITOL,
          D.RIND,
          D.COL1
        FROM CIS2.VW_DATA_ALL D
        WHERE
          D.PERIOADA IN (:pPERIOADA)
          AND D.FORM IN (:pFORM)
          AND D.CAPITOL = 1040 AND CAPITOL_VERS = 2013
          AND D.RIND IN ('1.6.1','1.6.2','1.6.3','1.6.4')
          AND D.CAEM2 NOT LIKE 'A%'
      ) A1
      LEFT JOIN CIS2.X_BAZA_SONDAJ BS ON (A1.CUIIO=BS.CUIIO AND BS.ANUL=2025)
      INNER JOIN (
        SELECT CODUL, FULL_CODE 
        FROM CIS2.VW_CL_CAEM2
        WHERE SUBSTR(CODUL,1,1) IN ('B','C','D','E','H','J','K') 
           OR SUBSTR(CODUL,1,3) IN ('G46','M71','M72','M73')
      ) C ON C.CODUL = A1.CAEM2
      CROSS JOIN 
      (
        SELECT 2 AS NR_TABLE, 'Clasa de marime' AS TABLE_DENUMIRE, 0 AS PERS_INIT, 999999 AS PERS_FINAL, NULL AS RIND, 'Total salariati' AS RIND_DENUMIRE, '1' AS ORDINE, 'B+C+D+E+H+J+K+G46+M71+M72+M73' AS RIND_C FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 0, 9,   NULL, '0-9 salariati',        '2',  'B+C+D+E+H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 10, 49, NULL, '10-49 salariati',       '3',  'B+C+D+E+H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 50, 249,NULL, '50-249 salariati',      '4',  'B+C+D+E+H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 250, 999999, NULL, '250 si peste salariati','5','B+C+D+E+H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 0, 999999, NULL, 'Industrie - total','6',  'B+C+D+E' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 0, 9,   NULL, '0-9 salariati',       '7',  'B+C+D+E' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 10, 49, NULL, '10-49 salariati',     '8',  'B+C+D+E' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 50, 249,NULL, '50-249 salariati',    '9',  'B+C+D+E' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 250, 999999, NULL, '250 si peste salariati', '10','B+C+D+E' FROM DUAL UNION 
        SELECT 2, 'Clasa de marime', 0, 999999, NULL, 'Servicii - total', '11','H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 0, 9,   NULL, '0-9 salariati',       '12','H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 10, 49, NULL, '10-49 salariati',     '13','H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 50, 249,NULL, '50-249 salariati',    '14','H+J+K+G46+M71+M72+M73' FROM DUAL UNION
        SELECT 2, 'Clasa de marime', 250, 999999, NULL, '250 si peste salariati','15','H+J+K+G46+M71+M72+M73' FROM DUAL
      ) A
      GROUP BY
        A.NR_TABLE, A.TABLE_DENUMIRE, A.RIND_DENUMIRE, A.ORDINE, A.RIND, A.PERS_INIT, A.PERS_FINAL, A.RIND_C
    ) A
    ORDER BY
      TO_NUMBER(A.NR_TABLE),
      TO_NUMBER(A.ORDINE)
  )

  UNION 

  SELECT 
    NR_TABLE,
    TABLE_DENUMIRE,
    RIND_DENUMIRE,
    ORDINE,
    RIND,
    COL1, COL2, COL3, COL4, COL5, COL6, COL7, COL8
  FROM
  (
    SELECT
      A.NR_TABLE,
      A.TABLE_DENUMIRE,
      A.RIND_DENUMIRE,
      A.ORDINE,
      A.RIND,

      /* ramura fara BS (CAEM2) – aceea?i logica: total pe prev.COL2, 1.6.x pe prev.COL3..prev.COL6 */
      /* prev.COL1 – nefolosit de mapping-ul final, îl pastram generic */
      ROUND(
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.1') AND NVAL(D.COL1)=1 THEN D.CUIIO END) +
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.2') AND NVAL(D.COL1)=1 THEN D.CUIIO END) +
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.3') AND NVAL(D.COL1)=1 THEN D.CUIIO END) +
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.4') AND NVAL(D.COL1)=1 THEN D.CUIIO END)
      ,0) AS COL1,

      /* prev.COL2 (=> final COL1) = TOTAL */
      (
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.1') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END) +
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.2') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END) +
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.3') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END) +
        COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.4') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END)
      ) AS COL2,

      /* prev.COL3..prev.COL6 (=> final COL2..COL5) = 1.6.1..1.6.4 */
      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.1') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END) AS COL3,
      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.2') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END) AS COL4,
      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.3') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END) AS COL5,
      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.4') AND NVAL(D.COL1)=1 AND (TR.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR TR.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%') THEN D.CUIIO END) AS COL6,

      /* prev.COL7..prev.COL8 => 0 */
      0 AS COL7,
      0 AS COL8
    FROM 
      CIS2.VW_DATA_ALL D 
      INNER JOIN (
        SELECT CODUL, FULL_CODE 
        FROM CIS2.VW_CL_CAEM2
        WHERE SUBSTR(CODUL,1,1) IN ('B','C','D','E','H','J','K')
           OR SUBSTR(CODUL,1,3) IN ('G46','M71','M72','M73')
      ) C ON C.CODUL = D.CAEM2
      CROSS JOIN 
      (
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'B+C+D+E+G+H+J+K+M' AS RIND, 'Total' AS RIND_DENUMIRE, '1' AS ORDINE FROM DUAL UNION 
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'B+C+D+E' AS RIND, 'Industrie - total' AS RIND_DENUMIRE, '2' AS ORDINE FROM DUAL  UNION
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'B' AS RIND, 'Industrie extractiva' AS RIND_DENUMIRE, '3' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'C' AS RIND, 'Industrie prelucratoare' AS RIND_DENUMIRE, '4' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'D' AS RIND, 'Productia si furnizarea de energie electrica si termica, gaze, apa calda si aer conditionat' AS RIND_DENUMIRE, '5' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'E' AS RIND, 'Distributia apei; salubritate, gestionarea deseurilor, activitati de decontaminare' AS RIND_DENUMIRE, '6' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'H+J+K+G+M' AS RIND, 'Servicii - total ' AS RIND_DENUMIRE, '7' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'G' AS RIND, 'Comert cu ridicata ' AS RIND_DENUMIRE, '8' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'H' AS RIND, 'Transport si depozitare ' AS RIND_DENUMIRE, '9' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'J' AS RIND, 'Informatii si comunicatii' AS RIND_DENUMIRE, '10' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, 'K' AS RIND, 'Activitati  financiare si asigurari' AS RIND_DENUMIRE, '11' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, '71' AS RIND, 'Activitati de arhitectura si inginerie' AS RIND_DENUMIRE, '12' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, '72' AS RIND, 'Activitati de cercetare dezvoltare' AS RIND_DENUMIRE, '13' AS ORDINE FROM DUAL UNION  
        SELECT 1 AS NR_TABLE, 'CAEM2' AS TABLE_DENUMIRE, NULL AS PERS_INIT, NULL AS PERS_FINAL, '73' AS RIND, 'Publicitate si activitati de studiere a pietei' AS RIND_DENUMIRE, '14' AS ORDINE FROM DUAL   
      ) TR
    WHERE
      D.PERIOADA IN (:pPERIOADA)
      AND D.FORM IN (:pFORM)
      AND D.RIND IN ('1.6.1','1.6.2','1.6.3','1.6.4')
      AND D.CAEM2 NOT LIKE 'A%'
    GROUP BY
      TR.NR_TABLE, TR.TABLE_DENUMIRE, TR.RIND_DENUMIRE, TR.ORDINE, TR.RIND
    ORDER BY
      TR.NR_TABLE, TO_NUMBER(TR.ORDINE)
  )

  UNION 

  /* Regiuni de dezvoltare – pastram structura, dar mutam TOTAL pe prev.COL2 (?i îl lasam ?i pe prev.COL1, fara efect în mapping) */
  SELECT 
    3 AS NR_TABLE, 
    ' Regiuni de dezvoltare' AS TABLE_DENUMIRE,
    NUME_ROW AS RIND_DENUMIRE,
    NR_ROW   AS ORDINE,
    CS_CUATM AS RIND,

    /* prev.COL1 (nefolosit la mapping final) = total clasic */
    (SUM(COL1) + SUM(COL2) + SUM(COL3) + SUM(COL4)) AS COL1,

    /* prev.COL2 (=> final COL1) = TOTAL 1.6.1..1.6.4 */
    (SUM(COL1) + SUM(COL2) + SUM(COL3) + SUM(COL4)) AS COL2, 

    /* prev.COL3..prev.COL6 (=> final COL2..COL5) = 1.6.1..1.6.4 */
    SUM(COL1) AS COL3, 
    SUM(COL2) AS COL4, 
    SUM(COL3) AS COL5,
    SUM(COL4) AS COL6,

    /* prev.COL7..prev.COL8 (=> final COL6..COL7) = 0 */
    0 AS COL7,
    0 AS COL8
  FROM
  (
    SELECT
      CC.NR_ROW,
      CC.CS_CUATM,
      CC.NUME_ROW,
      CC.ORDINE,

      /* aici, COL1..COL4 = 1.6.1..1.6.4; restul la 0 */
      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.1') AND C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' AND NVAL(D.COL1)=1
           AND (CC.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR CC.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%')
           THEN D.CUIIO END) AS COL1,

      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.2') AND C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' AND NVAL(D.COL1)=1
           AND (CC.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR CC.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%')
           THEN D.CUIIO END) AS COL2,

      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.3') AND C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' AND NVAL(D.COL1)=1
           AND (CC.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR CC.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%')
           THEN D.CUIIO END) AS COL3,

      COUNT(DISTINCT CASE WHEN D.RIND IN ('1.6.4') AND C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' AND NVAL(D.COL1)=1
           AND (CC.RIND LIKE '%'||SUBSTR(D.CAEM2,1,1)||'%' OR CC.RIND LIKE '%'||SUBSTR(D.CAEM2,2,2)||'%')
           THEN D.CUIIO END) AS COL4,

      0 AS COL5,
      0 AS COL6,
      0 AS COL7

    FROM 
      CIS2.VW_DATA_ALL D
      INNER JOIN CIS2.VW_CL_CUATM  C ON C.CODUL = D.CUATM
      CROSS JOIN (
        SELECT '01' AS NR_ROW, 'TOTAL'  AS NUME_ROW, '0000000'  AS CS_CUATM, 'B+C+D+E+H+J+K+G46+M71+M72+M73' AS RIND, 'Total' AS RIND_DENUMIRE, '1'  AS ORDINE FROM  DUAL UNION  
        SELECT '02' AS NR_ROW, 'MUN.CHISINAU'  AS NUME_ROW, '0100000'  AS CS_CUATM, 'B+C+D+E+H+J+K+G46+M71+M72+M73' AS RIND, 'Total' AS RIND_DENUMIRE, '2'  FROM  DUAL UNION
        SELECT '03' AS NR_ROW, 'NORD'  AS NUME_ROW, '1111111'  AS CS_CUATM, 'B+C+D+E+H+J+K+G46+M71+M72+M73' AS RIND, 'Total' AS RIND_DENUMIRE, '3'  FROM  DUAL UNION 
        SELECT '04' AS NR_ROW, 'CENTRU'  AS NUME_ROW, '2222222'  AS CS_CUATM, 'B+C+D+E+H+J+K+G46+M71+M72+M73' AS RIND, 'Total' AS RIND_DENUMIRE, '4'  FROM  DUAL UNION
