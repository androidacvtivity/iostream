SELECT 
   D.UNIT_CODE,
   D.UNIT_CODE_VERS,
   D.RIND,
   SUM(D.COL1) AS COL1,
   SUM(D.COL2) AS COL2,
   SUM(D.COL3) AS COL3
   
FROM
  CIS2.VW_DATA_ALL_GC  D

 
WHERE
  (D.PERIOADA IN (:pPERIOADA))  AND   
  (D.FORM =:pFORM) AND
  (D.FORM_VERS =:pFORM_VERS) AND 


 

  D.FORM IN (61)  AND
  D.CAPITOL IN (1110) 
  AND D.UNIT_CODE = 60404
--  AND D.RIND IN ('001')

  
  GROUP BY 
   D.UNIT_CODE,
   D.UNIT_CODE_VERS,
   D.RIND
  ORDER BY 
  D.RIND
  
