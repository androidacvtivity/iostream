 SELECT 
    --   L.CUIIO,
    
      DISTINCT  M.RIND,
       L.ID_MD,
       MAX(L.DATA_REG) DATA_REG, 
       SUM(L.COL1) AS COL1,
       SUM(L.COL2) AS COL2,
       SUM(L.COL3) AS COL3,
       SUM(L.COL4) AS COL4,
       SUM(L.COL5) AS COL5,
       SUM(L.COL6) AS COL6,
       SUM(L.COL7) AS COL7,
       SUM(L.COL8) AS COL8,
       SUM(L.COL9) AS COL9,
       SUM(L.COL10) AS COL10
        

FROM CIS2.DATA_ALL_LOG  L  INNER JOIN CIS2.MD_RIND M ON M.ID_MD = L.ID_MD

WHERE

L.CUIIO = 40337554 

AND L.FORM = 64

AND L.PERIOADA = 2012

AND L.ID_USER = 734

AND L.ID_MD IN (
77324,
59032,
59030,
58744,
58742,
58741,
58735,
58734

)
 AND 
 
 (L.DATA_REG < TO_DATE('01/15/2025 11:10:45', 'MM/DD/YYYY HH24:MI:SS') AND L.DATA_REG > TO_DATE('01/13/2025 11:10:45', 'MM/DD/YYYY HH24:MI:SS')) 
 
 
 GROUP BY
 --L.CUIIO,
       M.RIND,
       L.ID_MD
    --   L.DATA_REG
      
 
 ORDER BY 
 M.RIND
 --L.DATA_REG DESC 
 
 
 