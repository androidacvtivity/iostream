
SELECT 
DISTINCT D.CUIIO 
FROM
(
SELECT DISTINCT
  D.CUIIO,
  D.RIND,
  SUM(D.COL1) AS COL1

FROM
  CIS2.VW_DATA_ALL D

WHERE
  (D.PERIOADA=:PERIOADA         ) AND
 
   D.CAPITOL IN (100)
  AND D.FORM IN (64)
  AND D.RIND IN ('1','5','6')  

GROUP BY 
  D.CUIIO,
  D.RIND
  
  HAVING 
  NVAL(SUM(D.COL1)) > 0
  ) D 

