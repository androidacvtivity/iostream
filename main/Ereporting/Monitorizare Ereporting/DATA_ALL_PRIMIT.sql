select DISTINCT 
 CUIIO,
 CUIIO_VERS,
 CUATM,
 MAX(DATA_REG) DATA_REG,
 SUM(COL1)  COL1,
 SUM(COL2)  COL2,
 SUM(COL3)  COL3,
 SUM(COL4)  COL4,
 SUM(COL5)  COL5,
 SUM(COL6)  COL6,
 SUM(COL7)  COL7,
 SUM(COL8)  COL8 
 
 from USER_EREPORTING.VW_DATA_ALL_PRIMIT
 
 WHERE
 
 PERIOADA IN (:pPERIOADA)
             and CUIIO LIKE :pCUIIO||'%'


AND 

FORM IN (:pFORM)


GROUP BY

 CUIIO,
 CUIIO_VERS,
 CUATM

 
ORDER BY

DATA_REG  DESC
