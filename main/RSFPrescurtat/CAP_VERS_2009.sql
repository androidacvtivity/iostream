

SELECT 

    D.CUIIO,
    D.CAPITOL,
    D.CAPITOL_VERS,
    D.RIND,
    D.RIND_VERS,
    D.ID_MD

FROM CIS2.VW_DATA_ALL_FR D

WHERE

FORM = 63
AND CUIIO =  40856062 
AND PERIOADA = 2013

AND CAPITOL = 1120

--AND RIND IN ('060')


GROUP BY
   D.CUIIO,
    D.CAPITOL,
    D.CAPITOL_VERS,
    D.RIND,
    D.RIND_VERS,
    D.ID_MD
    
    ORDER BY
   D.CUIIO,
    D.CAPITOL,
 --   D.CAPITOL_VERS,
    D.RIND,
    D.RIND_VERS